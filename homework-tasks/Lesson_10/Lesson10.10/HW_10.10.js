const container = document.getElementById("container");
const prevBtn = document.getElementById("prev");
const nextBtn = document.getElementById("next");

let currentPage = 1;
const itemsPerPage = 10;
const totalPages = Math.ceil(coursesArray.length / itemsPerPage);

function renderPage(page) {
  container.innerHTML = "";

  const start = (page - 1) * itemsPerPage;
  const end = start + itemsPerPage;
  const items = coursesArray.slice(start, end);

  for (const course of items) {
    const div = document.createElement('div');
    div.classList.add('courseName');

    const titleDiv = document.createElement('div');
    titleDiv.classList.add('titleText');

    const h3 = document.createElement('h3');
    h3.classList.add('title');
    h3.innerText = course.title;
    titleDiv.appendChild(h3);

    const infoDiv = document.createElement('div');
    infoDiv.classList.add('textInfo');

    const p = document.createElement('p');
    p.classList.add('info');
    p.innerText = `Month - ${course.monthDuration}   Hours - ${course.hourDuration}`;
    infoDiv.appendChild(p);

    div.append(titleDiv, infoDiv);
    container.appendChild(div);
  }

  prevBtn.disabled = page === 1;
  nextBtn.disabled = page === totalPages;
}

prevBtn.addEventListener("click", () => {
  if (currentPage > 1) {
    currentPage--;
    renderPage(currentPage);
  }
});

nextBtn.addEventListener("click", () => {
  if (currentPage < totalPages) {
    currentPage++;
    renderPage(currentPage);
  }
});

renderPage(currentPage);